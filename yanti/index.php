Kenapa orang bisa hobi fotografi? Orang-orang bisa hobi fotografi dengan alasan sendiri-sendiri. Untuk menjawab pertanyaan ini anda harus menanyakan kepada yang bersangkutan. Baiklah, saya coba merangkum beberapa alasan mengapa orang bisa hobi fotografi. 

Alasan kenapa orang bisa hobi fotografi : - Karena suatu moment sulit untuk bisa diulang kembali. - Buat tambah teman dan mencari penghasilan tambahan. - Foto bisa menjadi bagian dari sebuah sejarah. Satu hal yang memiliki arti dari perjalanan waktu. - Foto juga bisa dijadikan sebuah kenangan. Bisa mengingatkan kejadian di masa lalu. - Fotografi itu unik dan tidak semua orang bisa menghasilkan foto sebagai sebuah karya seni. - Hobi fotografi itu bisa dekat dengan wanita-wanita / model cantik. - Karena ingin mengabadikan sesuatu, meski jelas bahkan dokumentasi itu tidak abadi. - Karena bisa jalan-jalan ke tempat menarik dan indah untuk berburu objek foto. - Fotografi menjadi semacam hiburan untuk melepaskan ketegangan dan tekanan / stress. - Fotografi itu seni. - Fotografii bisa dijadikan karir. - Karena sekarang sudah banyak kamera digital sehingga tidak repot lagi seberti jaman kamera analog. - Fotografi sebagai gaya hidup. - Sebuah foto atau gambar bisa menceritakan banyak hal. - Orang menyukai fotografi biasanya berawal memang suka menikmati keindahan dari sebuah gambar, kemudian berubah mulai mencoba mengabadikan moment melalui sebuah foto. Maka kamimelakukan terobosan perusahaan yang bekerja di bidang photography. jika anda ingin melihat beberapa karya kami silahkan lihat di : <br>
